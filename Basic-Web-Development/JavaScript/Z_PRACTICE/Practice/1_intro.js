// Print "Hello World"
console.log("Hello World!");

// Comments (single-line, multi-line)


// Variables (var, let, const)
var age = 10;
console.log(age);

let my_name = "RAJ";
console.log(my_name);

const psw = "6x#2t9@f&q7"
console.log(psw);

// Data types (string, number, boolean, null, undefined, object, symbol, bigint)

// string
let str = "i am string";
console.log(str);
console.log(typeof(str));

// number
let num = 34;
console.log(num);
console.log(typeof(num));

// boolean
let boolean = true;
console.log(boolean);
console.log(typeof(boolean));

// null
let Null = null;
console.log(Null);
console.log(typeof(Null));

// undefined
let und;
console.log(und);
console.log(typeof(und));

// object
let student = {
    name: "Raj",
    reg: 2421297023,
    sec: "C",
    dept: "CSE",
    college: "NIT"
};
console.log(student);
console.log(typeof(student));
console.log("Name:", student.name);
console.log("Reg:", student.reg);
console.log("College:", student.college);

// symbol
let mySymbol = Symbol("description");
console.log(mySymbol);
console.log(typeof(mySymbol));

// bigint
let bigInt = 345678908765432234n;
console.log(bigInt);
console.log(typeof(bigInt));


